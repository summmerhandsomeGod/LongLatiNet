class Conv2DMask(nn.Conv2d):

    def __init__(
            self,
            in_channels: int,
            out_channels: int,
            kernel_size: _size_2_t,
            stride: _size_2_t = 1,
            padding: Union[str, _size_2_t] = 0,
            dilation: _size_2_t = 1,
            groups: int = 1,
            bias: bool = True,
            padding_mode: str = 'zeros',  # TODO: refine this type
            device=None,
            dtype=None
    ) -> None:

        factory_kwargs: dict = {'device': device, 'dtype': dtype}
        kernel_size_ = _pair(kernel_size)
        stride_ = _pair(stride)
        padding_ = padding if isinstance(padding, str) else _pair(padding)
        dilation_ = _pair(dilation)

        super(Conv2DMask, self).__init__(
            in_channels=in_channels,
            out_channels=out_channels,
            kernel_size=kernel_size_,
            stride=stride_,
            padding=padding_,
            dilation=dilation_,
            groups=groups,
            bias=bias,
            padding_mode=padding_mode,
            **factory_kwargs
            )
        self.mask = torch.ones(
            (out_channels, in_channels // groups, *kernel_size_), **factory_kwargs)
        b, c, h, w = self.mask.shape
        for i in range(h):
            if i == (h - 1) / 2:
                self.mask[:, :, i: i + 1, :] = 1
            for j in range(w):
                if j == (w - 1) / 2:
                    self.mask[:, :, :, j: j + 1] = 1
                if ((h - 1) / 4 - 1) < i < (h - (h - 1) / 4) and ((w - 1) / 4 - 1) < j < (w - (w - 1) / 4):
                    self.mask[:, :, i: i + 1, j: j + 1] = 1
                if i % 2 == 0 and j % 2 == 0:
                    self.mask[:, :, i: i + 1, j: j + 1] = 1
        self.mask = Parameter(self.mask)

    def _conv_forward(self, input: Tensor, weight: Tensor, bias: Optional[Tensor]):
        if self.padding_mode != 'zeros':
            return F.conv2d(F.pad(input, self._reversed_padding_repeated_twice, mode=self.padding_mode),
                            weight, bias, self.stride,
                            _pair(0), self.dilation, self.groups)
        return F.conv2d(input, weight, bias, self.stride,
                        self.padding, self.dilation, self.groups)

    def forward(self, input: Tensor) -> Tensor:
        return self._conv_forward(input, self.weight * self.mask, self.bias)


class StarConv(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = Conv2DMask(c1, c2, k, s, autopad(k, p), groups=g, bias=False)
        self.bn = nn.InstanceNorm2d(c2, affine=True)
        # self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def forward_fuse(self, x):
        return self.act(self.conv(x))


